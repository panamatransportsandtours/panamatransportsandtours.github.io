/* SLIDER DE IMÃGENES */
const sliders = document.querySelectorAll('.image-slider');

sliders.forEach(slider => {
  const images = slider.querySelectorAll('img');
  let index = 0;
  let interval;

  function showImage(i) {
    images.forEach((img, idx) => {
      img.classList.toggle('active', idx === i);
    });
  }

  function startSlider() {
    if (images.length <= 1 || interval) return;
    interval = setInterval(() => {
      index = (index + 1) % images.length;
      showImage(index);
    }, 2000);
  }

  function stopSlider() {
    clearInterval(interval);
    interval = null;
    index = 0;
    showImage(index);
  }

  slider.addEventListener('mouseenter', startSlider);
  slider.addEventListener('mouseleave', stopSlider);
  slider.addEventListener('touchstart', startSlider);
  document.body.addEventListener('touchstart', function (e) {
    if (!slider.contains(e.target)) stopSlider();
  });

  showImage(0);
});

