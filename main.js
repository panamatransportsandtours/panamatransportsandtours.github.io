function openInfo(key) {
  const lang = localStorage.getItem("language") || "es";
  document.getElementById('info-content').innerHTML = (lang === "en" ? infoEn : info)[key];
  document.getElementById('info-panel').style.display = 'block';
  history.pushState({ panelOpen: true }, 'info-panel');
}

function closeInfo() {
  document.getElementById('info-panel').style.display = 'none';
  if (history.state && history.state.panelOpen) {
    history.back();
  }
}

window.addEventListener('click', function (event) {
  const panel = document.getElementById('info-panel');
  if (event.target === panel) closeInfo();
});

window.addEventListener('popstate', function () {
  const panel = document.getElementById('info-panel');
  if (panel && panel.style.display === 'block') {
    panel.style.display = 'none';
  }
});

document.addEventListener('click', function (event) {
  const panel = document.getElementById('info-panel');
  if (panel.style.display !== 'block') return;
  if (panel.contains(event.target) || event.target.closest('[onclick^="openInfo"]')) return;
  closeInfo();
});

document.addEventListener('keydown', function (event) {
  if (event.key === 'Escape') closeInfo();
});

/* SLIDER DE IMÁGENES */
const sliders = document.querySelectorAll('.image-slider');

sliders.forEach(slider => {
  const images = slider.querySelectorAll('img');
  let index = 0;
  let interval;

  function showImage(i) {
    images.forEach((img, idx) => {
      img.classList.toggle('active', idx === i);
    });
  }

  function startSlider() {
    if (images.length <= 1 || interval) return;
    interval = setInterval(() => {
      index = (index + 1) % images.length;
      showImage(index);
    }, 2000);
  }

  function stopSlider() {
    clearInterval(interval);
    interval = null;
    index = 0;
    showImage(index);
  }

  slider.addEventListener('mouseenter', startSlider);
  slider.addEventListener('mouseleave', stopSlider);
  slider.addEventListener('touchstart', startSlider);
  document.body.addEventListener('touchstart', function (e) {
    if (!slider.contains(e.target)) stopSlider();
  });

  showImage(0);
});

